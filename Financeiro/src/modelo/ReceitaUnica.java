package modelo;

public class ReceitaUnica {
	
	private String nome;
	private Double total;
	
	public ReceitaUnica(){
		
	}
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public Double getTotal() {
		return total;
	}
	public void setTotal(Double total) {
		this.total = total;
	}
	
	
	

}
